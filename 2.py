import numpy as np
import matplotlib.pyplot as plt
# x2 = np.array([-10,-7.5,-5,-4,-3,-2.5,-2,-1.5,-1,-0.5,0,0.5,1,1.5,2,2.5,3,4,5,7.5,10])
# y2 = [0.001,0.004,0.021,0.037,0.058,0.072,0.083,0.098,0.110,0.119,0.123,0.120,0.111,0.099,0.084,0.072,0.062,0.040,0.024,0.019,0.017]
# 设置圆环导线的参数
I = 1.0  # 圆环导线中心的电流强度（单位：安培）
R = 1.0  # 圆环导线的半径（单位：米）

# 创建一个表示距离的一维数组，以及一个用于存储磁场强度的空数组
r = np.arange(-10,10,1)  # 从0.1米到5米生成100个距离值
B = []  # 存储磁场强度的空数组

# 使用毕奥萨伐尔定律计算每个距离点上的磁场强度，并将结果存储在B数组中
for distance in r:
    # 毕奥萨伐尔定律计算磁场强度
    mu0 = 4 * np.pi * 1e-7  # 真空中的磁导率（单位：N/A^2）
    magnetic_field = (mu0 * I * R**2) / (2 * (R**2 + distance**2)**(3/2))
    B.append(magnetic_field)
print('磁场强度随距离的变化',B)
# 使用Matplotlib绘制磁场强度随距离的变化曲线
plt.figure(figsize=(8, 6))
plt.plot(r, B, label="磁场强度")
plt.xlabel("距离（米）")
plt.ylabel("磁场强度（特斯拉）")
plt.title("毕奥萨伐尔定律圆环导线磁场强度")

plt.legend()
plt.grid()
plt.show()
